#include "Road.hpp"

#include <iostream>

int Road::number_of_blocked_roads_{0};

//----------------------------------------------------------------------------------------------------------------------
Road::Road(bool blocked, Coordinates pos):
  Field(pos),
  blocked_{blocked}
{
  if (blocked){
    Road::number_of_blocked_roads_++;
  }
}

//----------------------------------------------------------------------------------------------------------------------
int Road::getNumberOfBlockedRoads()
{
  return Road::number_of_blocked_roads_;
}

//----------------------------------------------------------------------------------------------------------------------
bool Road::isBlocked(){
  return blocked_;
}

//----------------------------------------------------------------------------------------------------------------------
void Road::print() {
  char c;
  if (blocked_){
    c = '%';
  } else {
    c = '#';
  }

  std::cout << c;
}